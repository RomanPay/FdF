#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int	i;
	int	lendst;

	i = 0;
	lendst = ft_strlen(s1);
	while (s2[i] != '\0')
	{
		s1[lendst] = s2[i];
		i++;
		lendst++;
	}
	s1[lendst] = '\0';
	return (s1);
}
