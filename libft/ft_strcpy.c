#include "libft.h"

char	*ft_strcpy(char *dst, const char *src)
{
	char	*temp;

	temp = dst;
	while (*src)
	{
		*dst = *src;
		dst++;
		src++;
	}
	*dst = '\0';
	return (temp);
}
